public class ControlFlow {
    //For Loop
    public static void main(String[] args){
        int[] anArray = {10,20,30,40,50};

        for(int i = 0; i < anArray.length; i++){
            System.out.println(anArray[i]);
        }

        //For Each Loop
        int[] a = {60,70,80,90};

        for (int i: a){
            System.out.println(i);
        }

        
    }
        
}
